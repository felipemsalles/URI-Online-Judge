A, B, C = input().split(" ")
A = float(A)
B = float(B)
C = float(C)
triangle = (A * C) / 2
circle = 3.14159 * C ** 2
trapeze = ((A + B) * C) / 2
square = B * B
rectangle = A * B
print('TRIANGULO: %.3f' % triangle)
print('CIRCULO: %.3f' % circle)
print('TRAPEZIO: %.3f' % trapeze)
print('QUADRADO: %.3f' % square)
print('RETANGULO: %.3f' % rectangle)

# Portuguese --  English
# TRIÂNGULO  == TRIANGLE, CÍRCULO == CIRCLE, TRAPÉZIO == TRAPEZE, QUADRADO = SQUARE, RETÂNGULO == RECTANGLE.
